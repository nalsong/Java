package day1;

public class Test01 {
	
	public static void main(String[] args) { //메인메서드
		boolean bool = true; //boolean자료형의 변수bool에 true를 대입
		char ch = 'a'; // char자료형의 변수ch에 a를 대입
		byte b = 1; // byte자료형 변수b에 1을 대입
		short s = 2; // short자료형 변수s에 2를 대입
		int i =2; // int자료형 변수 i에 2를 대입
		double f = 3.14; // double자료형 f에 3.14를 대입
		
		System.out.println(bool); // 변수 bool의 값을 출력
		System.out.println(ch); //변수 ch의 값을 출력
		System.out.println(b); //변수 b의 값을 출력
		System.out.println(s); //변수 s의 값을 출력
		System.out.println(i); //변수 i의 값을 출력
		System.out.println(f); //변수 f의 값을 출력
	}
}
